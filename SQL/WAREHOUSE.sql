USE CEID_RESCUE_PROGRAM;

CREATE TABLE IF NOT EXISTS CATEGORIES(CATEGORY_NAME VARCHAR(50) NOT NULL PRIMARY KEY);
                                    
CREATE TABLE IF NOT EXISTS PRODUCTS(
									ID INT AUTO_INCREMENT NOT NULL PRIMARY KEY,
                                    CATEGORY VARCHAR(50) NOT NULL,
                                    PRODUCT_NAME VARCHAR(50) NOT NULL,
                                    DISCONTINUED BOOLEAN NOT NULL DEFAULT FALSE,
                                    
                                    FOREIGN KEY (CATEGORY) REFERENCES CATEGORIES(CATEGORY_NAME));
                                    
CREATE TABLE IF NOT EXISTS DETAILS_OF_PRODUCTS(
                                                PRODUCT INT NOT NULL,
                                                DETAIL_NAME VARCHAR(200) NOT NULL,
                                                DETAIL_VALUE VARCHAR(200) NOT NULL,
                                                
                                                PRIMARY KEY (PRODUCT, DETAIL_NAME, DETAIL_VALUE),
                                                FOREIGN KEY (PRODUCT) REFERENCES PRODUCTS(ID));

CREATE TABLE IF NOT EXISTS WAREHOUSE(
                                    PRODUCT INT NOT NULL PRIMARY KEY,
                                    AMOUNT INT NOT NULL DEFAULT 0,
                                    
                                    FOREIGN KEY(PRODUCT) REFERENCES PRODUCTS(ID));

CREATE TABLE IF NOT EXISTS VAN_LOAD(
                                    product INT NOT NULL,
                                    amount  INT NOT NULL,
                                    rescuer VARCHAR(50) NOT NULL,

                                    PRIMARY KEY (product, rescuer),
                                    FOREIGN KEY (product) REFERENCES PRODUCTS(ID),
                                    FOREIGN KEY (rescuer) REFERENCES USERS(USERNAME));

CREATE TABLE IF NOT EXISTS ANNOUNCEMENT(
                                        id      INT       NOT NULL AUTO_INCREMENT,
                                        product INT       NOT NULL,
                                        date    TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP(),

                                        PRIMARY KEY(id, product),
                                        FOREIGN KEY(product) REFERENCES PRODUCTS(ID));

CREATE TABLE IF NOT EXISTS UserOffersRequests(
                                            id INT AUTO_INCREMENT PRIMARY KEY NOT NULL,
                                            user VARCHAR(50) NOT NULL,
                                            type ENUM('request', 'offer') NOT NULL,
                                            createdOn TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP(),
                                            status ENUM('created', 'inTransition', 'completed', 'canceled') NOT NULL DEFAULT 'created',
                                            assumedBy VARCHAR(50) DEFAULT NULL,
                                            assumedOn TIMESTAMP DEFAULT NULL,
                                            completedOn TIMESTAMP DEFAULT NULL,

                                            FOREIGN KEY(user) REFERENCES USERS(USERNAME),
                                            FOREIGN KEY(assumedBy) REFERENCES USERS(USERNAME)
);

CREATE TABLE IF NOT EXISTS ProductsOffersRequests(
                                                offerId INT NOT NULL,
                                                product INT NOT NULL,
                                                amount INT NOT NULL,

                                                PRIMARY KEY(offerId, product),
                                                FOREIGN KEY(offerId) REFERENCES UserOffersRequests(id),
                                                FOREIGN KEY(product) REFERENCES PRODUCTS(id)
);



        INTO FOUND_PRODUCT_IN_WAREHOUSE
    FROM WAREHOUSE
    WHERE PRODUCT = FOUND_PRODUCT;

    IF FOUND_PRODUCT_IN_WAREHOUSE IS NULL
    THEN
        INSERT INTO WAREHOUSE(PRODUCT, AMOUNT) VALUES (FOUND_PRODUCT, 0);
    END IF;

    COMMIT;

END$$

CREATE PROCEDURE ADD_PRODUCTS_IN_WAREHOUSE(
										IN p_PRODUCT VARCHAR(50), 
										IN p_CATEGORY VARCHAR(50),
                                        IN p_CATEGORY_DESCRIPTION VARCHAR(200),
										IN p_AMOUNT INT)
BEGIN
	DECLARE CATEGORY_ID INT;
    DECLARE PRODUCT_ID INT;
    DECLARE ID_WAREHOUSE INT;
    DECLARE STORED_AMOUNT INT;
	
    SELECT ID INTO CATEGORY_ID
    FROM CATEGORIES
    WHERE CATEGORY_NAME = p_CATEGORY;
    
    SELECT ID INTO PRODUCT_ID
    FROM PRODUCTS
    WHERE PRODUCT_NAME = p_PRODUCT;
    
    IF CATEGORY_ID IS NULL THEN
		INSERT INTO CATEGORIES(CATEGORY_NAME, DESCRIPTION) VALUES (p_CATEGORY, p_CATEGORY_DESCRIPTION);
        SELECT ID INTO CATEGORY_ID
		FROM CATEGORIES
		WHERE CATEGORY_NAME = p_CATEGORY;
    END IF;
    
    IF PRODUCT_ID IS NULL THEN
		INSERT INTO PRODUCTS(CATEGORY, PRODUCT_NAME) VALUES(CATEGORY_ID, p_PRODUCT);
        SELECT ID INTO PRODUCT_ID
		FROM PRODUCTS
		WHERE PRODUCT_NAME = p_PRODUCT;
    END IF;

	SELECT ID INTO ID_WAREHOUSE
    FROM WAREHOUSE
    WHERE PRODUCT = PRODUCT_ID;
    
    IF ID_WAREHOUSE IS NULL THEN 
		INSERT INTO WAREHOUSE(PRODUCT, AMOUNT) VALUES (PRODUCT_ID, p_AMOUNT);
	ELSE
		SELECT AMOUNT INTO STORED_AMOUNT
        FROM WAREHOUSE
        WHERE ID = ID_WAREHOUSE; 
        
        
        UPDATE WAREHOUSE
        SET AMOUNT = p_AMOUNT + STORED_AMOUNT
        WHERE ID = ID_WAREHOUSE;
	END IF;

END$$

CREATE PROCEDURE UNLOAD_PRODUCTS(
                                IN p_PRODUCT_ID INT,
                                IN p_amount     INT,
                                IN p_rescuer    VARCHAR(50))
BEGIN

    START TRANSACTION;

    UPDATE WAREHOUSE
    SET AMOUNT = AMOUNT + p_amount
    WHERE PRODUCT = p_PRODUCT_ID;


    DELETE FROM VAN_LOAD
    WHERE product = p_PRODUCT_ID 
        AND rescuer = p_rescuer;

    COMMIT;

END$$

CREATE PROCEDURE LOAD_PRODUCTS(
                                IN p_product_id INT,
                                IN p_amount     INT,
                                IN p_rescuer    VARCHAR(50))
BEGIN
    DECLARE FOUND_PRODUCT INT DEFAULT NULL;

    START TRANSACTION;

    UPDATE WAREHOUSE
    SET AMOUNT = AMOUNT - p_amount
    WHERE PRODUCT = p_product_id;

    SELECT product
        INTO FOUND_PRODUCT
    FROM VAN_LOAD
    WHERE rescuer = p_rescuer
        AND product = p_product_id;

    IF FOUND_PRODUCT IS NULL THEN
        INSERT INTO VAN_LOAD(product, amount, rescuer) VALUES(p_product_id, p_amount, p_rescuer);
    ELSE
        UPDATE VAN_LOAD
        SET amount = amount + p_amount
        WHERE product = p_product_id;
    END IF;

    COMMIT;

END$$

CREATE PROCEDURE IF NOT EXISTS completeTask(
                                            IN username VARCHAR(50),
                                            IN taskId INT
)
BEGIN

    DECLARE taskType ENUM('request', 'offer');
    DECLARE prod INT;
    DECLARE prod_found INT DEFAULT NULL;
    DECLARE amou INT;

    DECLARE finished INT DEFAULT 0;

    DECLARE cursor_products CURSOR FOR
        SELECT product, amount
        FROM ProductsOffersRequests
        WHERE offerId = taskId;

    DECLARE CONTINUE HANDLER FOR NOT FOUND
    SET finished = 1;

    OPEN cursor_products;

    SELECT type INTO taskType
    FROM UserOffersRequests
    WHERE id = taskId;

    IF taskType = 'request' THEN

        SELECT product, amount INTO prod, amou
        FROM ProductsOffersRequests
        WHERE offerId = taskId;

        START TRANSACTION;

        UPDATE VAN_LOAD
        SET amount = amount - amou
        WHERE rescuer = username
            AND product = prod;

        UPDATE UserOffersRequests
        SET completedOn = CURRENT_TIMESTAMP(),
            status = 'completed'
        WHERE id = taskId;

        COMMIT;
    ELSE

        START TRANSACTION;

        REPEAT
            FETCH cursor_products INTO prod, amou;

            IF NOT finished THEN
                SELECT COUNT(*) INTO prod_found
                FROM VAN_LOAD
                WHERE rescuer = username
                    AND product = prod;

                IF prod_found = 0 THEN
                    INSERT INTO VAN_LOAD(rescuer, amount, product) VALUES(username, amou, prod);
                ELSE
                    UPDATE VAN_LOAD
                    SET amount = amount + amou
                    WHERE rescuer = username
                        AND product = prod;
                END IF;
            END IF;

        UNTIL finished END REPEAT;

        UPDATE UserOffersRequests
        SET completedOn = CURRENT_TIMESTAMP(),
            status = 'completed'
        WHERE id = taskId;

        COMMIT;

    END IF;

    CLOSE cursor_products;

END $$

